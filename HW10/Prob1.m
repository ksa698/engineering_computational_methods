Vandermonde_Matrix=A\b

%% b) Graphing Newton
x=[.25 .125 .375 0 .5];
    x1=linspace(0,0.5);
y=[7.75 6.24 4.85 0 0];

f1=0.28801365+62.3402*x1-92.67*x1.^2-246.655*x1.^3+365.268*x1.^4;
    fb1=7.75.*x1.^0; 
    fb2=12.08.*(x1-0.25);
    fb3=-141.12.*(x1-.25).*(x1-.125);
    fb4=27.296.*(x1-.25).*(x1-.125).*(x1-.375);
    fb5=365.268.*(x1-.25).*(x1-.125).*(x1-.375).*x1;
    figure

plot(x,y,'o')
title('Newton')
hold on
plot(x1,f1,x1,fb1,x1,fb2,x1,fb3,x1,fb4,x1,fb5)

legend('X & Y','Interpolation','Base 1','Base 2','Base 3', 'Base 4', 'Base 5')

%% c) Graphing LaGrange 
    L1=((x1-.125).*(x1-.375).*x1.*(x1-.5))/((.25-.125).*(.25-.375).*.25.*(.25-.5));
    L2=((x1-.25).*(x1-.375).*x1.*(x1-.5))/((.125-.25).*(.125-.375).*.125.*(.125-.5));
    L3=((x1-.25).*(x1-.125).*x1.*(x1-.5))/((.375-.25).*(.375-.125).*.375.*(.375-.5));
    L4=((x1-.25).*(x1-.125).*(x1-.375).*(x1-.5))/((-.25).*(-.375).*(-.125).*(-.5));
    L5=((x1-.25).*(x1-.125).*(x1-.375).*(x1))/((.5-.25).*(.5-.375).*(.5-.125).*(.5));
g1=(7.75*L1)+(6.24*L2)+(4.85*L3);    
    
