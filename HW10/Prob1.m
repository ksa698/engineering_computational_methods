%% Part a) Vandermonde Matrix
A=[1 0 0 0 0;...
   1 .125 .015625 .001953 .000244;...
   1 .25 .0625 .015625 .003906;...
   1 .375 .140625 .052734 .019775;...
   1 .5 .25 .125 .0625];
